class CAvailCasterScenId  
{
	////////////////////////////////////////////////////////////////
	//	
	//	Represents the ids of caster scenarios available to load 
	//    from the archive database.
	//	
	////////////////////////////////////////////////////////////////


	////////////////////////////////////////////////////////////////
	//	
	//	Typedefs, enums, etc.
	//	
	////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////
	//	
	//	Data
	//	
	////////////////////////////////////////////////////////////////

private:

	long m_id;
	int m_caster;
	CString m_owner;
	CTime m_createTime;
	CTime m_lastSaveTime;
	int m_version;
	CString m_comment;
	bool m_isLoaded;
	bool m_isPublic;


	////////////////////////////////////////////////////////////////
	//	
	//	Accessors
	//	
	////////////////////////////////////////////////////////////////

public:

	long& Id()				{ return m_id;				}
	int& Caster()			{ return m_caster;			}
	CString& Owner()		{ return m_owner;			}
	CTime& CreateTime()		{ return m_createTime;		}
	CTime& LastSaveTime()	{ return m_lastSaveTime;	}
	int& Version()			{ return m_version;			}
	CString& Comment()		{ return m_comment;			}
	bool& IsLoaded()		{ return m_isLoaded;		}
	bool& IsPublic()		{ return m_isPublic;		}


	////////////////////////////////////////////////////////////////
	//	
	//	C-tors, d-tor
	//	
	////////////////////////////////////////////////////////////////

public:
	CAvailCasterScenId();
	virtual ~CAvailCasterScenId();


	////////////////////////////////////////////////////////////////
	//	
	//	Operators
	//	
	////////////////////////////////////////////////////////////////

public:

	bool operator<(const CAvailCasterScenId& x) const
	{	return m_id < x.m_id;	}

	bool operator==(const CAvailCasterScenId& x) const
	{	return m_id == x.m_id;	}


	////////////////////////////////////////////////////////////////
	//	
	//	Operations
	//	
	////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////
	//	
	//	Implementation
	//	
	////////////////////////////////////////////////////////////////


};

